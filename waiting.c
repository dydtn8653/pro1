#include <stdio.h>
#include <stdbool.h>
#include <unistd.h>
#include <pthread.h>
#include <stdatomic.h>

#define N 8             /* 스레드 개수 */
#define RUNTIME 100000  /* 출력량을 제한하기 위한 실행시간 (마이크로초) */

char *color[N+1] = {"\e[0;30m","\e[0;31m","\e[0;32m","\e[0;33m","\e[0;34m","\e[0;35m","\e[0;36m","\e[0;37m","\e[0m"};

atomic_flag lock = ATOMIC_FLAG_INIT;
volatile int current_thread = 0; // 현재 실행중인 스레드의 인덱스
volatile bool alive = true;

void spin_lock() {
    while (atomic_flag_test_and_set_explicit(&lock, memory_order_acquire)) {
        // 스핀락 대기
    }
}

void spin_unlock() {
    atomic_flag_clear_explicit(&lock, memory_order_release);
}

void *worker(void *arg) {
    int i = *(int *)arg;
    int runtime = 0;

    while (alive) {
        if (current_thread != i) {
            usleep(1); // 다음 스레드를 기다림
            continue;
        }

        spin_lock();
        /*
         * 임계구역: 알파벳 문자를 한 줄에 40개씩 10줄 출력한다.
         */
        for (int k = 0; k < 400; ++k) {
            printf("%s%c%s", color[i], 'A'+i, color[N]);
            if ((k+1) % 40 == 0)
                printf("\n");
        }
        /*
         * 임계구역이 성공적으로 종료되었다.
         */
        spin_unlock();
        
        // 다음 스레드를 위해 current_thread를 업데이트
        current_thread = (current_thread + 1) % N;

        runtime++;
        if (runtime >= RUNTIME)
            break;
    }
    pthread_exit(NULL);
}

int main(void)
{
    pthread_t tid[N];
    int i, id[N];

    /*
     * N 개의 자식 스레드를 생성한다.
     */
    for (i = 0; i < N; ++i) {
        id[i] = i;
        pthread_create(tid+i, NULL, worker, id+i);
    }
    /*
     * 스레드가 출력하는 동안 RUNTIME 마이크로초 쉰다.
     * 이 시간으로 스레드의 출력량을 조절한다.
     */
    usleep(RUNTIME);
    /*
     * 스레드가 자연스럽게 무한 루프를 빠져나올 수 있게 한다.
     */
    alive = false;
    /*
     * 자식 스레드가 종료될 때까지 기다린다.
     */
    for (i = 0; i < N; ++i)
        pthread_join(tid[i], NULL);
    /*
     * 메인함수를 종료한다.
     */
    return 0;
   
}

